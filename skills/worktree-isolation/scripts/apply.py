#!/usr/bin/env -S uv run --script --quiet
# /// script
# requires-python = ">=3.11"
# dependencies = ["pyyaml"]
# ///
"""Worktree isolation entry point.

Two callers:
  1. Claude Code SessionStart hook → `apply.py --quiet` (emits hook JSON).
  2. worktree-isolation skill → `apply.py --verbose --force` or `--status`.

Reads the project's docker-compose*.yml files, generates per-worktree override
files (sibling .worktree.yml) with deterministic randomized host ports + suffixed
container_names, writes .env.worktree, and registers the generated files in the
per-worktree git exclude list.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))

import yaml  # noqa: E402

from lib import (  # noqa: E402
    PortMapping,
    collect_container_names,
    collect_port_mappings,
    ensure_git_exclude,
    find_compose_files,
    overrides_up_to_date,
    override_path_for,
    render_env_worktree,
    render_override_yaml,
    worktree_id,
)


def _build_summary(
    project_name: str,
    worktree: str,
    compose_chain: list[tuple[Path, Path | None]],
    mappings: list[PortMapping],
) -> str:
    lines = [
        f"Worktree isolation active: project='{project_name}' (id={worktree}).",
        "",
        "Port mappings (legacy → worktree host):",
    ]
    for m in sorted(mappings, key=lambda x: (x.service, x.container_port)):
        lines.append(
            f"  {m.service}  container :{m.container_port}  →  host {m.new_host_port}  "
            f"(was {m.original_host_port})"
        )
    lines += [
        "",
        "To bring up the stack from this worktree:",
        "  set -a; source .env.worktree; set +a",
        "  docker compose up -d",
        "(COMPOSE_FILE in .env.worktree auto-chains base + override files.)",
        "",
        "When running ad-hoc commands (e.g. test commands in CLAUDE.md) that hardcode",
        "legacy host ports like " + ", ".join(sorted({str(m.original_host_port) for m in mappings}))
        + ", substitute the matching new host port from the table above before executing.",
    ]
    return "\n".join(lines)


def _run(args: argparse.Namespace) -> int:
    cwd = args.cwd.resolve()

    if args.status:
        env_file = cwd / ".env.worktree"
        if not env_file.exists():
            print(f"No .env.worktree at {cwd}. Run with --force to generate.", file=sys.stderr)
            return 1
        print(env_file.read_text())
        return 0

    compose_files = find_compose_files(cwd)
    if not compose_files:
        if args.quiet:
            print(json.dumps({"continue": True}))
        elif args.verbose:
            print(f"No docker-compose files in {cwd}. Nothing to isolate.")
        return 0

    wid = worktree_id(cwd)
    project_name = f"{cwd.name}-{wid}"
    env_file = cwd / ".env.worktree"

    compose_chain: list[tuple[Path, Path | None]] = []
    all_mappings: list[PortMapping] = []
    regenerated_any = False

    for base in compose_files:
        mappings = collect_port_mappings(base, wid)
        container_names = collect_container_names(base)

        if not mappings and not container_names:
            # Nothing to override for this file — still include in chain for completeness? No,
            # if there's nothing to override, the base alone is fine. Skip.
            continue

        override_path = override_path_for(base)

        if not args.force and overrides_up_to_date(base, override_path, env_file):
            compose_chain.append((base, override_path))
            all_mappings.extend(mappings)
            continue

        override_yaml_data = render_override_yaml(mappings, container_names, project_name, wid)
        with open(override_path, "w") as f:
            f.write("# Auto-generated by ~/.claude/scripts/worktree-isolation/apply.py\n")
            f.write("# DO NOT COMMIT. Regenerate with the worktree-isolation skill.\n")
            yaml.safe_dump(override_yaml_data, f, sort_keys=False, default_flow_style=False)
        compose_chain.append((base, override_path))
        all_mappings.extend(mappings)
        regenerated_any = True

    if not compose_chain:
        if args.quiet:
            print(json.dumps({"continue": True}))
        elif args.verbose:
            print(f"No services with ports or container_name in {cwd}. Nothing to isolate.")
        return 0

    # Always write env file when we have a chain — covers fresh dirs even if overrides exist.
    env_content = render_env_worktree(project_name, wid, compose_chain, all_mappings)
    if not env_file.exists() or env_file.read_text() != env_content:
        env_file.write_text(env_content)
        regenerated_any = True

    ensure_git_exclude(cwd, [".env.worktree", "*.worktree.yml", "*.worktree.yaml"])

    summary = _build_summary(project_name, wid, compose_chain, all_mappings)

    if args.quiet:
        out = {
            "hookSpecificOutput": {
                "hookEventName": "SessionStart",
                "additionalContext": summary,
            }
        }
        print(json.dumps(out))
    else:
        print(summary)
        if not regenerated_any:
            print("\n(no changes — overrides already up to date)")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(description="Per-worktree docker-compose isolation.")
    parser.add_argument("--cwd", type=Path, default=Path.cwd(), help="Working directory")
    parser.add_argument("--quiet", action="store_true", help="Hook mode: emit JSON only")
    parser.add_argument("--verbose", action="store_true", help="Skill mode: human output")
    parser.add_argument("--force", action="store_true", help="Regenerate even if up-to-date")
    parser.add_argument("--status", action="store_true", help="Print current .env.worktree only")
    args = parser.parse_args()

    try:
        return _run(args)
    except Exception as exc:  # noqa: BLE001 — fail loud; hook captures stderr
        if args.quiet:
            # Don't break the session — emit a minimal valid hook response and log to stderr.
            print(json.dumps({"continue": True}))
            print(f"worktree-isolation error: {exc}", file=sys.stderr)
            return 0
        raise


if __name__ == "__main__":
    sys.exit(main())
